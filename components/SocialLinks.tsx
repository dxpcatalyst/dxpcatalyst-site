// Social media icon links. Each icon renders only when its URL is set in
// siteSettings, so nothing shows broken. Icons inherit text color via
// currentColor (gray by default, brand blue on hover).

type Props = {
  linkedInUrl?: string;
  instagramUrl?: string;
  className?: string;
  // Color styling for the icon links. Defaults to the light-header treatment
  // (gray → brand blue on hover); the footer overrides it for the navy bg.
  linkClassName?: string;
};

export function SocialLinks({ linkedInUrl, instagramUrl, className, linkClassName }: Props) {
  if (!linkedInUrl && !instagramUrl) return null;

  const linkClasses = linkClassName ?? 'text-gray-600 transition hover:text-brand';

  return (
    <div className={`flex items-center gap-3 ${className ?? ''}`}>
      {linkedInUrl && (
        <a
          href={linkedInUrl}
          target="_blank"
          rel="noopener noreferrer"
          aria-label="LinkedIn"
          className={linkClasses}
        >
          <LinkedInIcon className="h-5 w-5" />
        </a>
      )}
      {instagramUrl && (
        <a
          href={instagramUrl}
          target="_blank"
          rel="noopener noreferrer"
          aria-label="Instagram"
          className={linkClasses}
        >
          <InstagramIcon className="h-5 w-5" />
        </a>
      )}
    </div>
  );
}

